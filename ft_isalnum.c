#include <stdio.h>
int	ft_isalnum (int c)
{
	if ((c >= 48 && c <= 57) || (c >= 65  && c <= 90) || (c >= 97 && c <= 122))
		return (1);
	return (0);
}
int main ()
{
	int q;
	q = '5';
	printf("%d", ft_isalnum(q));
}	
