int ft_strcmp( const char *s1, const char *s2, size_t n)
{
	size_t i;

	i = 0;
	while ((s1[1] != '\0' || s2[i] != '\0' ) && i < n)
	{
		if(s1[i] != s2[i])
			return ((size_t) s1[i] - (size_t) s2[i]);
		i++;
	}
	return (0);
}	
